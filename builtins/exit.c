#include "../minishell.h"

int	ft_exit(char *cmd)
{
	char	**splt;

	splt = ft_split(cmd, ' ');
	if (!splt[1])
		exit(0);
	else if (splt[1])
		exit(ft_atoi(splt[1]));
	return (1);
}
