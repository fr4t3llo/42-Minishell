int ft_expand(char *cmd)
{
    int i;

    i = 0;
    while (cmd[i])
    {
        if (cmd[i] == '$' && cmd[i + 1] == '?')
        {
            printf("this is expend !!!\n");
            return (0);
        }
        i++;
    }
    return (0);
}